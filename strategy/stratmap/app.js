      this.isExporting=false;

      this.ctx=savedCtx; this.cw=savedCw; this.ch=savedCh;
      this.camera=savedCam; this.activeVpId=savedVpId;

      const imgDataOverview=ocOverview.toDataURL('image/jpeg', 0.8);
