  _addBox(type){
    const box={
      id:uid(), type, title: type === 'text' ? 'Text' : `New ${this._cap(type)}`,
      x:this.camera.cx + (Math.random()-0.5)*0.3,
      y:this.camera.cy + (Math.random()-0.5)*0.3,
      w:BOX_W, h:BOX_H, rot: 0,
      z:this.state.nextZ++,
      vpId: type === 'text' ? this.activeVpId : null,
      fontSize: type === 'text' ? 16 : undefined,
      bgColor: type === 'text' ? 'transparent' : undefined,
      fontFamily: type === 'text' ? 'var(--font-ink)' : undefined,
    };
    // Clamp to visible
    box.x=clamp(box.x,BOX_W/2+0.02,1-BOX_W/2-0.02);
    box.y=clamp(box.y,BOX_H/2+0.02,1-BOX_H/2-0.02);
    this.state.boxes.push(box);
    this._setMode('SELECT');
    this.selection={type:'box',id:box.id};
    this._updatePanel();
    this._updateGroupMembership();
    this._pushHistory(`Add ${this._cap(type)}`);
    this._autosave();
  }

