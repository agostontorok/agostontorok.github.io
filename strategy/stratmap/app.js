  _animateCamera(tx,ty,tz){
    const start={cx:this.camera.cx,cy:this.camera.cy,zoom:this.camera.zoom};
    const dur=600; const t0=performance.now();
    const tick=(now)=>{
      const p=clamp((now-t0)/dur,0,1);
      const ease=p<0.5?2*p*p:(4-2*p)*p-1; // ease in-out quad
      this.camera.cx=lerp(start.cx,tx,ease);
      this.camera.cy=lerp(start.cy,ty,ease);
      this.camera.zoom=lerp(start.zoom,tz,ease);
      this._updateZoomDisplay();
      if(p<1) requestAnimationFrame(tick);
    };
    requestAnimationFrame(tick);
  }

