function uid() {
  return `e_${Date.now().toString(36)}_${Math.random().toString(36).substr(2,6)}`;
}
function clamp(v,a,b){ return Math.min(b,Math.max(a,v)); }
function lerp(a,b,t){ return a+(b-a)*t; }
function dist2(ax,ay,bx,by){ return (ax-bx)**2+(ay-by)**2; }

function ptSegDist(px,py,ax,ay,bx,by){
  const dx=bx-ax,dy=by-ay,l2=dx*dx+dy*dy;
  if(l2===0) return Math.sqrt(dist2(px,py,ax,ay));
  const t=clamp(((px-ax)*dx+(py-ay)*dy)/l2,0,1);
  return Math.sqrt(dist2(px,py,ax+t*dx,ay+t*dy));
}

function rdp(pts,eps){
  if(pts.length<3) return pts;
  let maxD=0,idx=0;
  const a=pts[0],b=pts[pts.length-1];
  for(let i=1;i<pts.length-1;i++){
    const d=perpDist(pts[i],a,b);
    if(d>maxD){maxD=d;idx=i;}
  }
  if(maxD>eps){
    const L=rdp(pts.slice(0,idx+1),eps);
    const R=rdp(pts.slice(idx),eps);
    return [...L.slice(0,-1),...R];
  }
  return [a,b];
}

function perpDist(pt,a,b){
  const dx=b.x-a.x,dy=b.y-a.y;
  const len=Math.sqrt(dx*dx+dy*dy);
  if(len<1e-10) return Math.sqrt((pt.x-a.x)**2+(pt.y-a.y)**2);
  return Math.abs(dx*(a.y-pt.y)-dy*(a.x-pt.x))/len;
}

function pointInPolygon(px,py,polygon){
  let inside=false;
  const n=polygon.length;
  for(let i=0,j=n-1;i<n;j=i++){
    const xi=polygon[i].x,yi=polygon[i].y;
    const xj=polygon[j].x,yj=polygon[j].y;
    if((yi>py)!==(yj>py)&&px<(xj-xi)*(py-yi)/(yj-yi)+xi) inside=!inside;
  }
  return inside;
}

