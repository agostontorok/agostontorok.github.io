  _exportPNG(dpi){
    try {
      // CSS pixels × (dpi/96) = output pixels  (96dpi = standard screen)
      const scale = dpi / 96;
      const oc  = document.createElement('canvas');
      oc.width  = Math.round(this.cw * scale);
      oc.height = Math.round(this.ch * scale);
      const octx = oc.getContext('2d');
      if (!octx) throw new Error("Could not get 2d context");
      octx.scale(scale, scale);
      // Swap context temporarily (keep cw/ch the same — they're CSS px)
      const savedCtx = this.ctx;
      this.ctx = octx;
      this.isExporting = true;
      this._render();
      this.isExporting = false;
      this.ctx = savedCtx;
      oc.toBlob(blob=>{
        try {
          if(!blob){ throw new Error('PNG export failed — canvas may be too large.'); }
          this._download(URL.createObjectURL(blob), `stratmap-${Date.now()}.png`);
        } catch(err) {
          console.error(err);
          this.showToast("Export failed: " + err.message);
        }
      }, 'image/png');
    } catch(err) {
      console.error(err);
      this.showToast("Export failed: " + err.message);
    }
  }

