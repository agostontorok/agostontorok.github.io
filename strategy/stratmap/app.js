  // Draw a closed Catmull-Rom spline through screen-space points.
  // Uses the standard conversion to cubic bezier for each segment.
  _drawSplinePath(ctx, pts) {
    const n = pts.length;
    if (n < 3) return;
    const get = i => pts[((i % n) + n) % n];
    ctx.beginPath();
    ctx.moveTo(pts[0].x, pts[0].y);
    for (let i = 0; i < n; i++) {
      const p0 = get(i - 1), p1 = get(i), p2 = get(i + 1), p3 = get(i + 2);
      // Catmull-Rom → Cubic Bezier control points (tension = 1/6)
      const cp1x = p1.x + (p2.x - p0.x) / 6;
      const cp1y = p1.y + (p2.y - p0.y) / 6;
      const cp2x = p2.x - (p3.x - p1.x) / 6;
      const cp2y = p2.y - (p3.y - p1.y) / 6;
      ctx.bezierCurveTo(cp1x, cp1y, cp2x, cp2y, p2.x, p2.y);
    }
    ctx.closePath();
  }

  // Apply elastic deformation: dragged vertex moves exactly to cursor;
  // neighbors follow with exponential decay. All computed from original
  // path snapshot to avoid accumulated drift.
  _elasticDeformGroup(g, dragIdx, rawDx, rawDy) {
    const FALLOFF = [1.0, 0.55, 0.22, 0.07, 0.02];
    const orig = this.resizeGroupPathStart;
    const n = orig.length;

    // Tension / resistance: compress large drags logarithmically
    const rawDist = Math.sqrt(rawDx * rawDx + rawDy * rawDy);
    const MAX_FREE = 0.18; // world units before resistance kicks in
    let edx = rawDx, edy = rawDy;
    if (rawDist > MAX_FREE) {
      const compressed = MAX_FREE + (rawDist - MAX_FREE) * 0.35;
      const scale = compressed / rawDist;
      edx *= scale;
      edy *= scale;
    }

    g.path = orig.map((p, i) => {
      // Shortest circular distance between index i and dragIdx
      const fwd  = Math.abs(i - dragIdx);
      const bwd  = n - fwd;
      const hops = Math.min(fwd, bwd);
      const factor = hops < FALLOFF.length ? FALLOFF[hops] : 0;
      return { x: p.x + edx * factor, y: p.y + edy * factor };
    });
  }

  // Arc-length redistribution → evenly spaced nodes, then RDP clean-up.
  // Called once on mouse-up to prevent point clustering.
  _redistributeGroupPath(path) {
    const n = path.length;
    if (n < 4) return path;

    // 1. Cumulative arc lengths around the closed polygon
    const segLens = [];
    let total = 0;
    for (let i = 0; i < n; i++) {
      const p1 = path[i], p2 = path[(i + 1) % n];
      const d = Math.sqrt((p2.x - p1.x) ** 2 + (p2.y - p1.y) ** 2);
      segLens.push(d);
      total += d;
    }
    if (total < 1e-6) return path;

    // 2. Choose target point count: enough to keep shape, not too many
    const targetN = Math.max(8, Math.min(48, Math.round(total / 0.035)));

    // 3. Resample evenly
    const cumLen = [0];
    for (let i = 0; i < n; i++) cumLen.push(cumLen[i] + segLens[i]);

    const newPath = [];
    for (let k = 0; k < targetN; k++) {
      const t = (k / targetN) * total;
      // Find which segment t falls in
      let seg = 0;
      while (seg < n - 1 && cumLen[seg + 1] <= t) seg++;
      const segStart = cumLen[seg];
      const segEnd   = cumLen[seg + 1];
      const alpha    = segEnd > segStart ? (t - segStart) / (segEnd - segStart) : 0;
      const p1 = path[seg], p2 = path[(seg + 1) % n];
      newPath.push({
        x: p1.x + (p2.x - p1.x) * alpha,
        y: p1.y + (p2.y - p1.y) * alpha,
      });
    }

    // 4. RDP to remove redundant co-linear points
    const simplified = rdp(newPath, RDP_EPSILON * 0.8);
    return simplified.length >= 4 ? simplified : newPath;
  }

